/*
    Description:
    The split() method splits a String object into an array 
    of strings by separating the string into substrings.

    Syntax:
    str.split([separator][, limit])
*/
/*
    Description:
    The substring() method returns a subset of a string 
    between one index and another, or through the end of
    the string.

    Syntax:
    str.substring(indexA[, indexB])
*/
/*
    Description:
    The lastIndexOf() method returns the index
     within the calling String object of the last occurrence of the 
     specified value, or -1 if not found. 
     The calling string is searched backward, starting at fromIndex.

    Syntax:
    str.lastIndexOf(searchValue[, fromIndex])
*/

/*
    Description:
    The search() method executes a search for a match 
    between a regular expression and this String object.

    Syntax:
    str.search(regexp)
*/